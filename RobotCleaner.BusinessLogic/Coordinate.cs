namespace RobotCleaner.BusinessLogic {

	/// <summary>
	/// Represents coordinate data.
	/// </summary>
	public class Coordinate {

		/// <summary>
		/// Gets or sets the X coordinate.
		/// </summary>
		/// <value>
		/// The X coordinate.
		/// </value>
		public int X { get; set; }

		/// <summary>
		/// Gets or sets the Y coordinate.
		/// </summary>
		/// <value>
		/// The Y coordinate.
		/// </value>
		public int Y { get; set; }

	}

}
